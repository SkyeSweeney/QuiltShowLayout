class Logger(wx.Panel):
        

    ####################################################################
    #
    ####################################################################
    def __init__(self, parent):
        wx.Panel.__init__(self, parent)

        
        txt = wx.StaticText(self, -1,
                            """This is a "Errors" object""",
                            (60, 60))
        self.win = None
        self.debug = False
    #


    ####################################################################
    #
    ####################################################################
    def SetWindow(self, win):
        self.win = win
    #    


    ####################################################################
    #
    ####################################################################
    def SetDebug(self, b):
        self.debug = b
    #    


    ####################################################################
    #
    ####################################################################
    def LogError(self, error):
        if self.win == None:
            print("ERR: %s" % error)
        else:
            pass
        #
    #
    

    ####################################################################
    #
    ####################################################################
    def LogWarning(self, warning):
        if self.win == None:
            print("WRN: %s" % warning)
        else:
            pass
        #
    #


    ####################################################################
    #
    ####################################################################
    def LogNotice(self, notice):
        if self.win == None:
            print("NTC: %s" % notice)
        else:
            pass
        #
     #


    ####################################################################
    #
    ####################################################################
    def LogDebug(self, debug):
        if self.debug:
            if self.win == None:
                print("DBG: %s" % debug)
